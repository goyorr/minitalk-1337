#include "libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*r;
	int		i;

	r = (char *) s;
	i = ft_strlen(r);
	while (i >= 0)
	{
		if (r[i] == (char)c)
			return (&r[i]);
		i--;
	}
	return (0);
}
