#include "libft.h"

void	ft_striteri(char *s, void (*f)(unsigned int, char*))
{
	int	i;
	int	a;

	if (!s || !f)
		return ;
	i = 0;
	a = ft_strlen(s);
	while (i < a)
	{
		f(i, &s[i]);
		i++;
	}
}
